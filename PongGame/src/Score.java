
public class Score 
{
	int rank, score;
	String name;
	
	public Score(int rank, String name, int score)
	{
		this.rank = rank;
		this.name = name;
		this.score = score;
	}

	public int getRank() {
		return rank;
	}

	public int getScore() {
		return score;
	}

	public String getName() {
		return name;
	}
	
	
	
}
